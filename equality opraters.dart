void main() {
  //equality & relational operater
  int number = 20;
  int number2 = 2;
  //equal to
  print(number == number2);

  //not equal
  print(number != number2);

  //less than
  print(number < number2);

  //greater
  print(number > number2);

  //Greater than or equal to
  print(number <= number2);
  //less than or equal to
  print(number >= number2);
}
