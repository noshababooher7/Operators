void main() {
  //logical operater
  num number1 = 10;
  num number2 = 20;
  //  // AND
  // Both conditions must be true
  print(number1 < 100 && number2 == 10);
  //number is true && number2 is false
  //true + false is
  //false

  // OR
  // One of the conditions must be true
  print(number1 == 100 || number2 == 20);
  //number1 is false || number2 is true
      // is one candition is true is 
      //true



  // NOT
  // Reverse the result, returns false if the result is true
  print(!(number1 == number2));
  //number1 and number2 is reverse in  false to true
  //true 



}
